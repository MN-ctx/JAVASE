public class Demo4 {
	
	/*
		4. 定义float和long变量的注意事项
		
				定义float类型变量的使用: 需要在数值的后面加入F的标识,F可以大写也可以小写.
				
				定义long类型变量的使用 : 需要在数值的后面加入L的标识,L可以大写也可以小写.
				
						但是, 建议大写
	*/
	public static void main(String[] args){
		// 定义float类型变量的使用: 需要在数值的后面加入F的标识,F可以大写也可以小写.
		float a = 12.3F;
		
		// 定义long类型变量的使用 : 需要在数值的后面加入L的标识,L可以大写也可以小写.
		long b = 1000L;
		
		System.out.println(a);
		System.out.println(b);
	}
}