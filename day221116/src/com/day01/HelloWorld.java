package com.day01;

public class HelloWorld {// 这是通过class定义了一个类，类名叫HelloWorld
    /*
        这是程序的入口点
        main方法，也称之为主方法
        如果程序没有编写主方法，将在运行的使用出现错误
    */
    public static void main(String[] args) {
        // 这是一条输出语句，可以在控制台打印双引号中所包裹的内容
        System.out.println("HelloWorld");
    }
}
